	@Override
	public byte[] getPlayerById(String team, String season, String league) {
		CloseableHttpClient httpClient = HttpClients.createDefault();
		List<Player> players = new ArrayList<>();
		Map<String, Object> ts = new HashMap<>();
		try {
			URI uri = URIBuilderUtil.buildURI("https://v3.football.api-sports.io/players",team,season,league);
			HttpGet httpGet = new HttpGet(uri);
			httpGet.setHeader("x-rapidapi-key", apiKey);

			CloseableHttpResponse response = httpClient.execute(httpGet);
			String responseBody = EntityUtils.toString(response.getEntity());
			//String responseBody = respo();

			ObjectMapper objectMapper = new ObjectMapper();
			JsonNode rootNode = objectMapper.readTree(responseBody).get("response");

			players = processPlayerInfo(rootNode,team,season,league);

		} catch (IOException | URISyntaxException e) {
			e.printStackTrace();
		}

		byte[] byteArray = generatePlayersReport(players);

		return byteArray;
	}


	private List<Player> processPlayerInfo(JsonNode node, String team, String season, String league) throws FileNotFoundException, IOException {
		List<Player> players = new ArrayList<>();
		for (JsonNode playerNode : node) {

			PlayerStatistics ps = extractPlayerStats(playerNode.get("statistics"),team,season,league);

			Player p = new Player();
			p.setPid(playerNode.get("player").get("id").asInt());
			p.setName(playerNode.get("player").get("name").asText());
			p.setAge(playerNode.get("player").get("age").asInt());
			p.setBirthdate(playerNode.get("player").get("birth").get("date").asText());
			p.setCountry(playerNode.get("player").get("nationality").asText());
			p.setHeight(playerNode.get("player").get("height").asInt());
			p.setWeight(playerNode.get("player").get("weight").asInt());
			p.setPhoto(playerNode.get("player").get("photo").asText());

			p.setStatistics(ps);

			players.add(p);

			pdao.save(p); // Save
		}

		return players;
	}

	private PlayerStatistics extractPlayerStats(JsonNode playerStats,String tid,String season,String league) throws JsonMappingException, JsonProcessingException {
		ObjectMapper mapper = new ObjectMapper();
		PlayerStatistics ps = new PlayerStatistics();
		TeamStatistics ts = new TeamStatistics();
		for (JsonNode stats : playerStats) {
			JsonNode teams = stats.get("team");
			JsonNode cards = stats.get("cards");
			JsonNode goals = stats.get("goals");
			JsonNode shots = stats.get("shots");
			JsonNode passes = stats.get("passes"); //$F{statistics.cards}.get("red")
			//JsonNode penalty = stats.get("penalty");

			Team t = tserv.saveTeam(teams,tid,season,league);
			ts = tdao.findByTeam(t);
			t.setTeamStatistics(ts);

			//System.out.println(t.getId()+" "+t.getTid());

			Map<String, Integer> cardMap = mapper.convertValue(cards, new TypeReference<Map<String, Integer>>() {});
			Map<String, Integer> shotmap = mapper.convertValue(shots, new TypeReference<Map<String, Integer>>() {});
			Map<String, Integer> passMap = mapper.convertValue(passes, new TypeReference<Map<String, Integer>>() {});
			EnumMap<GoalType, Integer> egoalMap = mapper.convertValue(goals, new TypeReference<EnumMap<GoalType,Integer>>() {});
			//Map<String, Integer> penaltyMap = mapper.convertValue(penalty, new TypeReference<Map<String, Integer>>() {} );
			//Map<String, Integer> game = mapper.convertValue(games, new TypeReference<Map<String, Integer>>() {});

			ps.setCards(cardMap);
			ps.setGoals(egoalMap);
			ps.setShots(shotmap);
			ps.setPasses(passMap);
			ps.setTeam(t);
		}
		return ps;
	}


	public byte[] generatePlayersReport(List<Player> players) {
		ByteArrayOutputStream output = new ByteArrayOutputStream();
		try {

			JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(players);
			JRBeanCollectionDataSource teamStatDataSource = new JRBeanCollectionDataSource(players);
		
			
			//Main report
			InputStream input = getClass().getResourceAsStream("/TableTest.jrxml");
			JasperDesign design = JRXmlLoader.load(input);
			JasperReport report = JasperCompileManager.compileReport(design);
			
			//SubReport
			InputStream subreportInput = getClass().getResourceAsStream("/teamstat.jrxml");
			JasperDesign subreportDesign = JRXmlLoader.load(subreportInput);
			JasperReport subreport = JasperCompileManager.compileReport(subreportDesign);
			
			//Parameters
			Map<String, Object> parameters = new HashMap<>();
			parameters.put("defaultValue", 0);
			parameters.put("CollectionBeanParam",dataSource);
			parameters.put("statBean",teamStatDataSource);
			parameters.put("SUBREPORT_PARAMETER", subreport);
			parameters.put("subList", players);
			
			JasperPrint print = JasperFillManager.fillReport(report, parameters, new JREmptyDataSource());

			JasperExportManager.exportReportToPdfStream(print, output);
			
			System.out.println("PDF generated");
		} catch (Exception e) {
			e.printStackTrace();
		}
		return output.toByteArray();
	}
	
